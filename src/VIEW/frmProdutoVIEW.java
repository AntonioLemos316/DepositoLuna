    private void listarValoresProdutos() {
        try {

            ProdutoDAO objprodutodao = new ProdutoDAO();

            DefaultTableModel model = (DefaultTableModel) tabelaProduto.getModel();
            model.setNumRows(0);

            ArrayList<ProdutoDTO> lista = objprodutodao.PesquisarProduto();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_produto(),
                    lista.get(num).getProduto_argamassa(),
                    lista.get(num).getProduto_cal_reboco(),
                    lista.get(num).getProduto_cimento()
                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores VIEW: " + erro);

        }
    }

    private void CarregarCampos() {
        int setar = tabelaProduto.getSelectedRow();

        txtCodigo.setText(tabelaProduto.getModel().getValueAt(setar, 0).toString());
        spinArgamassa.setValue(tabelaProduto.getModel().getValueAt(setar, 1));
        spinCalReboco.setValue(tabelaProduto.getModel().getValueAt(setar, 2));
        spinCimento.setValue(tabelaProduto.getModel().getValueAt(setar, 3));
        

    }

    private void FinalizarCadastro() {
        int prod_cimento, prod_argamassa, prod_cal_reboco;

        prod_cimento = (int) spinCimento.getValue();
        prod_argamassa = (int) spinArgamassa.getValue();
        prod_cal_reboco = (int) spinCalReboco.getValue();

        ProdutoDTO objprodutodto = new ProdutoDTO();
        objprodutodto.setProduto_cimento(prod_cimento);
        objprodutodto.setProduto_argamassa(prod_argamassa);
        objprodutodto.setProduto_cal_reboco(prod_cal_reboco);

        ProdutoDAO objprodutodao = new ProdutoDAO();
        objprodutodao.finalizarCadastroProd(objprodutodto);

    }

    private void LimparCampos() {
        txtCodigo.setText("");
        spinArgamassa.setValue(0);
        spinCalReboco.setValue(0);
        spinCimento.setValue(0);
        spinArgamassa.requestFocus();

    }

    private void AlterarProduto() {
        if (txtCodigo.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "CARREGUE OS CAMPOS");
        } else {
            int id_produto, produto_argamassa, produto_cal_reboco, produto_cimento;

            id_produto = Integer.parseInt(txtCodigo.getText());
            produto_argamassa = (int) spinArgamassa.getValue();
            produto_cal_reboco = (int) spinCalReboco.getValue();
            produto_cimento = (int) spinCimento.getValue();

            ProdutoDTO objprodutodto = new ProdutoDTO();
            objprodutodto.setId_produto(id_produto);
            objprodutodto.setProduto_argamassa(produto_argamassa);
            objprodutodto.setProduto_cal_reboco(produto_cal_reboco);
            objprodutodto.setProduto_cimento(produto_cimento);

            ProdutoDAO objprodutodao = new ProdutoDAO();
            objprodutodao.AlterarProduto(objprodutodto);
        }

    }

    private void ExcluirProduto() {
        if (txtCodigo.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "CARREGAR OS CAMPOS");
           
        } else {
            int id_produto, produto_argamassa, produto_cal_reboco, produto_cimento;

            id_produto = Integer.parseInt(txtCodigo.getText());

            ProdutoDTO objprodutodto = new ProdutoDTO();
            objprodutodto.setId_produto(id_produto);

            ProdutoDAO objprodutodao = new ProdutoDAO();
            objprodutodao.ExcluirProduto(objprodutodto);
        }
    }

